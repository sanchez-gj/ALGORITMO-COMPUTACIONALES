#CALCULAR EL AREA Y PERMIETRO DE UN CUADRADO
#AREA = L**2
#PERIMETRO = 4*L
#VARIABLES ENTRADA : L
#VARIABLES SALIDA : AREA, PERIMETRO

L= int(input("Ingrese el valor del lado del cuadrado: "))
AREA = L**2
PERIMETRO = 4*L
print("El area del cuadrado es: ", AREA)
print("El perimetro del cuadrado es: ", PERIMETRO)

